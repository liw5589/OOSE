package com.SE.Sell;

public class sellCustomerInfo {

	private String customerName;
	private int interestedCustomer;
	
	public sellCustomerInfo() {}
	
	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public int getInterestedCustomer() {
		return interestedCustomer;
	}

	public void setInterestedCustomer(int interestedCustomer) {
		this.interestedCustomer = interestedCustomer;
	}
}
